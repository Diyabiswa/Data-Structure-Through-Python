# List of numbers
numbers = [10, 21, 32, 43, 54, 65, 76, 87, 98]

# Loop through each number in the list
for num in numbers:
    # Check if the number is even or odd using if-else
    if num % 2 == 0:
        print(f"{num} is Even")
    else:
        print(f"{num} is Odd")

